package Lesson9.Domashka;

public class Triangle {
    private int a;
    private int b;
    private int c;

    public Triangle() {

    }

    public Triangle(int a1, int b1, int c1) {
        a = a1;
        b = b1;
        c = c1;
    }

    public void setA(int a1) {
        a = a1;
    }

    public void setB(int b1) {
        b = b1;
    }

    public void setC(int c1) {
        c = c1;
    }
}
