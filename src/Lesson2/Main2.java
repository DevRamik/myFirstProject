package Lesson2;

public class Main2 {
    public static void main(String[] args) {
        int score = 5;

        if (score == 5) {
            System.out.println("Молодец");
        }
        if (score < 4) {
            System.out.println("Хорошо");
        }
        if (score > 3) {
            System.out.println("Неуд");
        }
        if (score <= 2) {
            System.out.println("Плохо");
        } else {
            System.out.println("Иди учись");
        }
    }


}
